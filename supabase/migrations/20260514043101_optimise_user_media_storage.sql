create or replace function public.attach_pending_listing_media(
  p_listing_id bigint,
  p_owner_id uuid,
  p_avatar text,
  p_photos text[]
)
returns table (
  id bigint
)
language plpgsql
set search_path = public
as $$
declare
  normalised_avatar text := nullif(btrim(p_avatar), '');
  photo_paths text[] := coalesce(p_photos, array[]::text[]);
  deleted_count integer;
begin
  if cardinality(photo_paths) > 5 then
    raise exception 'max_photos_per_listing'
      using errcode = '23514', constraint = 'max_photos_per_listing';
  end if;

  if exists (
    select 1
    from unnest(photo_paths) as photo(path)
    where nullif(btrim(photo.path), '') is null
  ) then
    raise exception 'invalid_listing_photo_path'
      using errcode = '23514', constraint = 'valid_listing_photo_paths';
  end if;

  if (
    select count(*)
    from unnest(photo_paths) as photo(path)
  ) <> (
    select count(distinct photo.path)
    from unnest(photo_paths) as photo(path)
  ) then
    raise exception 'duplicate_listing_photo_path'
      using errcode = '23514', constraint = 'unique_listing_photo_paths';
  end if;

  perform 1
  from public.listings
  where listings.id = p_listing_id
    and listings.owner_id = p_owner_id
  for update;

  if not found then
    raise exception 'listing_media_attach_failed'
      using errcode = '23514', constraint = 'listing_media_attach_owner_check';
  end if;

  if normalised_avatar is not null then
    delete from public.pending_media_uploads
    where bucket = 'listing_avatars'
      and kind = 'listing_avatar'
      and path = normalised_avatar
      and user_id = p_owner_id;

    get diagnostics deleted_count = row_count;

    if deleted_count <> 1 then
      raise exception 'pending_listing_avatar_not_found'
        using errcode = '23514', constraint = 'pending_listing_avatar_owner_check';
    end if;
  end if;

  if cardinality(photo_paths) > 0 then
    delete from public.pending_media_uploads
    where bucket = 'listing_photos'
      and kind = 'listing_photo'
      and path = any(photo_paths)
      and user_id = p_owner_id;

    get diagnostics deleted_count = row_count;

    if deleted_count <> cardinality(photo_paths) then
      raise exception 'pending_listing_photos_not_found'
        using errcode = '23514', constraint = 'pending_listing_photos_owner_check';
    end if;
  end if;

  update public.listings
  set
    avatar = normalised_avatar,
    photos = photo_paths
  where listings.id = p_listing_id
    and listings.owner_id = p_owner_id;

  return query
  select p_listing_id;
end;
$$;

