create or replace function public.create_pending_media_upload(
  p_bucket text,
  p_kind text,
  p_path text,
  p_user_id uuid
)
returns void
language plpgsql
set search_path = public
as $$
begin
  if p_bucket not in ('listing_avatars', 'listing_photos') then
    raise exception 'invalid_pending_media_bucket'
      using errcode = '23514', constraint = 'pending_media_uploads_bucket_check';
  end if;

  if p_kind not in ('listing_avatar', 'listing_photo') then
    raise exception 'invalid_pending_media_kind'
      using errcode = '23514', constraint = 'pending_media_uploads_kind_check';
  end if;

  if (
    (p_kind = 'listing_avatar' and p_bucket <> 'listing_avatars')
    or (p_kind = 'listing_photo' and p_bucket <> 'listing_photos')
  ) then
    raise exception 'invalid_pending_media_bucket_kind_pair'
      using errcode = '23514', constraint = 'pending_media_uploads_bucket_kind_pair_check';
  end if;

  if p_kind = 'listing_avatar' then
    perform pg_advisory_xact_lock(
      hashtextextended('pending_listing_avatars:' || p_user_id::text, 0)
    );

    if (
      select count(*)
      from public.pending_media_uploads
      where user_id = p_user_id
        and bucket = 'listing_avatars'
        and kind = 'listing_avatar'
        and created_at >= now() - interval '1 day'
    ) >= 5 then
      raise exception 'max_pending_listing_avatars'
        using errcode = '23514', constraint = 'max_pending_listing_avatars';
    end if;
  elsif p_kind = 'listing_photo' then
    perform pg_advisory_xact_lock(
      hashtextextended('pending_listing_photos:' || p_user_id::text, 0)
    );

    if (
      select count(*)
      from public.pending_media_uploads
      where user_id = p_user_id
        and bucket = 'listing_photos'
        and kind = 'listing_photo'
        and created_at >= now() - interval '1 day'
    ) >= 5 then
      raise exception 'max_pending_listing_photos'
        using errcode = '23514', constraint = 'max_pending_listing_photos';
    end if;
  end if;

  insert into public.pending_media_uploads (bucket, kind, path, user_id)
  values (p_bucket, p_kind, p_path, p_user_id);
end;
$$;

revoke all on function public.create_pending_media_upload(text, text, text, uuid)
